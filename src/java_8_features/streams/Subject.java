package java_8_features.streams;

public class Subject {
	private String sname;
	private int score;
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public Subject(String sname, int score) {
		super();
		this.sname = sname;
		this.score = score;
	}
	
	
	
}
